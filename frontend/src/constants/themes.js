export const AVAILABLE_THEMES = [...LIGHT_THEMES, ...DARK_THEMES, ...SPECIAL_THEMES];

// Theme validation utility
export const isValidTheme = (themeName) => {
  return AVAILABLE_THEMES.some(theme => theme.name === themeName);
};

// Get theme by name
export const getThemeByName = (themeName) => {
  return AVAILABLE_THEMES.find(theme => theme.name === themeName);
};

// Check if theme is dark
export const isDarkTheme = (themeName) => {
  return DARK_THEMES.some(theme => theme.name === themeName);
};

// Default theme
export const DEFAULT_THEME = "dark";

// Theme search and filtering utilities
export const searchThemes = (query) => {
  if (!query.trim()) return AVAILABLE_THEMES;
  
  const searchTerm = query.toLowerCase().trim();
  return AVAILABLE_THEMES.filter(theme => 
    theme.name.toLowerCase().includes(searchTerm) ||
    theme.displayName.toLowerCase().includes(searchTerm)
  );
};

export const getThemesByCategory = (category) => {
  switch (category) {
    case 'light':
      return LIGHT_THEMES;
    case 'dark':
      return DARK_THEMES;
    case 'special':
      return SPECIAL_THEMES;
    default:
      return AVAILABLE_THEMES;
  }
};

export const getThemeCategory = (themeName) => {
  if (LIGHT_THEMES.some(theme => theme.name === themeName)) return 'light';
  if (DARK_THEMES.some(theme => theme.name === themeName)) return 'dark';
  if (SPECIAL_THEMES.some(theme => theme.name === themeName)) return 'special';
  return null;
};